package com.example.demo;

import java.util.Date;

public class Student {

   public int id ;
   public String name;
   public String email;
   public Date dob;

    public Student(int id, String name, String email, Date dob) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.dob = dob;
    }
}
